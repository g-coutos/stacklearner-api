import os

class Config:
	SECRET_KEY = os.getenv('SECRET_KEY')
	SESSION_COOKIE_DOMAIN = os.getenv('CONFIG_SESSION_COOKIE_DOMAIN')
	SESSION_COOKIE_HTTPONLY = os.getenv('CONFIG_SESSION_COOKIE_HTTPONLY', 'True') == 'True'
	SESSION_COOKIE_SECURE = os.getenv('CONFIG_SESSION_COOKIE_SECURE', 'True') == 'True'
	SESSION_COOKIE_SAMESITE = os.getenv('CONFIG_SESSION_COOKIE_SAMESITE')
	SESSION_TYPE = 'filesystem'
	SESSION_FILE_THRESHOLD = int(os.getenv('CONFIG_SESSION_FILE_THRESHOLD', 1))
	
	SESSION_PERMANENT = os.getenv('CONFIG_SESSION_PERMANENT', 'True') == 'True'
	PERMANENT_SESSION_LIFETIME = int(os.getenv('CONFIG_PERMANENT_SESSION_LIFETIME', 3600))  # Default to 1 hour
	
	# Database configuration
	SQLALCHEMY_DATABASE_URI = os.getenv('SQLALCHEMY_DATABASE_URI')
	SQLALCHEMY_TRACK_MODIFICATIONS = False
	DEBUG = os.getenv('DEBUG')
